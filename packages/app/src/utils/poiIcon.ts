export function generatePOIIcon(color: string, category: string, saved = false): ImageData {
  const canvas = document.createElement('canvas')
  canvas.width = SIZE
  canvas.height = SIZE
  const ctx = canvas.getContext('2d')!

  // Outer gap ring for saved POIs (drawn first, behind main circle)
  if (saved) {
    ctx.beginPath()
    ctx.arc(CX, CY, R + 5, 0, Math.PI * 2)
    ctx.strokeStyle = '#ffffff'
    ctx.lineWidth = 2.5
    ctx.stroke()
  }

  // Main circle
  ctx.beginPath()
  ctx.arc(CX, CY, R, 0, Math.PI * 2)
  ctx.fillStyle = color
  ctx.fill()

  // Border — only for saved POIs
  if (saved) {
    ctx.strokeStyle = 'rgba(255,255,255,0.85)'
    ctx.lineWidth = 10
    ctx.stroke()
  }

  // White Phosphor icon, scaled from 256×256 to fit inside the circle (R*1.5 wide)
  const pathData = PATHS[category]
  if (pathData) {
    const iconSize = R * 1.55         // desired rendered icon size in px
    const scale = iconSize / 256
    const offset = CX - iconSize / 2  // center inside canvas

    ctx.save()
    ctx.translate(offset, offset)
    ctx.scale(scale, scale)
    ctx.fillStyle = '#ffffff'
    ctx.fill(new Path2D(pathData))
    ctx.restore()
  }

  return ctx.getImageData(0, 0, SIZE, SIZE)
}
